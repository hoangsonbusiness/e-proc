import type * as Monaco from 'monaco-editor';

// ─── Helper (local) ────────────────────────────────────────────────────────

type MI = Monaco.languages.CompletionItem;
type MIK = Monaco.languages.CompletionItemKind;

function m(
  monaco: typeof Monaco,
  label: string,
  insertText: string,
  detail: string,
  doc: string,
  kind: MIK
): MI {
  return {
    label,
    kind,
    detail,
    documentation: { value: doc, isTrusted: true },
    insertText,
    insertTextRules: monaco.languages.CompletionItemInsertTextRule.InsertAsSnippet,
    range: undefined as unknown as Monaco.IRange,
  };
}

// shorthand aliases used below
const meth = (mo: typeof Monaco, l: string, t: string, d: string, doc: string) =>
  m(mo, l, t, d, doc, mo.languages.CompletionItemKind.Method);
const snip = (mo: typeof Monaco, l: string, t: string, d: string, doc: string) =>
  m(mo, l, t, d, doc, mo.languages.CompletionItemKind.Snippet);

